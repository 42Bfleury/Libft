#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst)
		return ;
	if (del)
		del(lst->content);
	lst->content = NULL;
	lst->next = NULL;
	free(lst);
	lst = NULL;
}
